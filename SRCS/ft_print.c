#include "ft_header.h"

void	ft_print(char **data)
{
	int i;
	int j;

	i = 0;
	j = 0;
	while (data[i][j])
	{
		while (data[i][j])
		{
			ft_putchar(data[i][j]);
			j++;
		}
		ft_putchar('\n');
		j = 0;
		i++;
	}
}
